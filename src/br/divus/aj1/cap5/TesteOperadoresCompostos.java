/**
 * TesteOperadoresCompostos - AJ1 - Código 28 - Página 90
 */
public class TesteOperadoresCompostos {

	public static void main(String[] args) {
		int n1 = 2;
		n1 *= 12;
		System.out.println(n1);
		
		n1 = 4;
		n1 += 4;
		System.out.println(n1);

		n1 = 9;
		n1 /= 3;
		System.out.println(n1);

		n1 = 5;
		n1 -= 5;
		System.out.println(n1);
	}

}
