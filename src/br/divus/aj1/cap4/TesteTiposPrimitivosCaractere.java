package br.divus.aj1.cap4;
/**
 * TesteTiposPrimitivosCaractere - Código 5 - AJ1 - Página 57
 */
public class TesteTiposPrimitivosCaractere {

    public static void main(String[] args) {
        
        char c = 'a';
        System.out.println(" c = 'a' ==> c = " + c);

        char asc = 64;
        System.out.println(" asc = 64 ==> asc = " + asc);

        char espaco = '\u0000';
        System.out.println(" espaco = '\\u0000' ==> espaco = " + espaco);

        char carinha = '\u0001';
        System.out.println(" carinha = '\\u0001' ==> carinha = " + carinha);
    }
}