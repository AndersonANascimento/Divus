package br.divus.aj1.cap6;

public class QuestaoIfElse {
	public static void main(String[] args) {
		int f5 = 59;
		if (f5 > 80) {
			System.out.println("f5 é maior que 80");
		} else if (f5 < 80) {
			System.out.println("f5 é menor que 80");
		} else if (f5 = 80) {
			System.out.println("f5 é igual a 80");
		}
	}
}