public class TesteSemForEach {

	public static void main(String[] args) {
		int arr[] = { 1, 2, 3, 4 };

		// Navegação usando o for tradicional em um array
		for (int i = 0; i < arr.length; i++) {
			System.out.println("\t [" + i + "] => " + arr[i]);
		}
	}

}
